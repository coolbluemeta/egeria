package org.odpi.openmetadata.frameworks.openmetadata.properties.datadictionaries;


import com.fasterxml.jackson.annotation.JsonAutoDetect;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;
import org.odpi.openmetadata.frameworks.openmetadata.properties.ClassificationBeanProperties;
import org.odpi.openmetadata.frameworks.openmetadata.types.OpenMetadataType;

import static com.fasterxml.jackson.annotation.JsonAutoDetect.Visibility.NONE;
import static com.fasterxml.jackson.annotation.JsonAutoDetect.Visibility.PUBLIC_ONLY;

/**
 * ObjectIdentifierProperties provides the bean for the OpjectIdentifier classification.
 */
@JsonAutoDetect(getterVisibility=PUBLIC_ONLY, setterVisibility=PUBLIC_ONLY, fieldVisibility=NONE)
@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonIgnoreProperties(ignoreUnknown=true)
public class ObjectIdentifierProperties extends ClassificationBeanProperties
{
    /**
     * Default constructor used by subclasses
     */
    public ObjectIdentifierProperties()
    {
        super();
        super.typeName = OpenMetadataType.OBJECT_IDENTIFIER_CLASSIFICATION.typeName;
    }


    /**
     * Copy/clone Constructor.
     *
     * @param template template object to copy.
     */
    public ObjectIdentifierProperties(ObjectIdentifierProperties template)
    {
        super(template);
    }


    /**
     * Standard toString method.
     *
     * @return print out of variables in a JSON-style
     */
    @Override
    public String toString()
    {
        return "ObjectIdentifierProperties{} " + super.toString();
    }
}